lanche = ('suco','pizza','refrigerante','pudim')

print(lanche)         #mostra apenas a variavel (que e uma tupla)
print(lanche[1])    #mostra a variavel na posicao 1
print(lanche[1:3])  #mostra a variavel da posicao [1] ate : posicao[3]
print(lanche[:2])  #mostra a variavel da posicao [0] ate : posicao[2]

# uma variavel tupla e IMUTAVEL seu valor nao muda 